function buildAgeBandSlotPhrases(label: string, focus: string): PremiumAgeBandSlotPhrases {
  return {
    flow: FLOW_SEEDS.map((seed) => `${label}에는 ${seed.replace("{focus}", focus)}`),
    fill: FILL_SEEDS.map((seed) => seed),
    caution: CAUTION_SEEDS.map((seed) => seed),
  };
}

