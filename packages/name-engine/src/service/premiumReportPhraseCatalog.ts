export const PREMIUM_REPORT_PHRASE_CATALOG: PremiumReportPhraseCatalog = {
  toneOpeners: TONE_OPENERS,
  ageBandSlots: AGE_BAND_SLOTS,
  conditionAdjusters: CONDITION_ADJUSTERS,
  connectors: CONNECTORS,
};

export function countPremiumReportPhrases(
  catalog: PremiumReportPhraseCatalog = PREMIUM_REPORT_PHRASE_CATALOG,
): number {
  const ageBandCount = (Object.values(catalog.ageBandSlots) as PremiumAgeBandSlotPhrases[]).reduce(
    (sum, slot) => sum + slot.flow.length + slot.fill.length + slot.caution.length,
    0,
  );

  return (
    catalog.toneOpeners.length +
    ageBandCount +
    catalog.conditionAdjusters.length +
    catalog.connectors.length
  );
}
